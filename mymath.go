package mymath

import (
	"math"
)

func Sqrt(x float64) int {
	return int(math.Sqrt(x))
}

func Abs(x float64) float64 {
	return math.Abs(x)
}
func Acos(x float64) float64 {
	return math.Acos(x)
}
func Acosh(x float64) float64 {
	return math.Acosh(x)
}
func Asin(x float64) float64 {
	return math.Asin(x)
}
